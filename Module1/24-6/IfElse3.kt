fun main()
{

    var marks = 56

    if(marks>=70)
    {
        println("A Grade")
    }
    if(marks>=60)
    {
        println("B Grade")
    }
    if(marks>=50)
    {
        println("C Grade")
    }
    if(marks>=40)
    {
        println("D Grade")
    }
    else
    {
        println("Fail")
    }


}